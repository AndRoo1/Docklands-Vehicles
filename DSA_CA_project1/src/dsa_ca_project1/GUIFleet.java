    private void updateFleetDisplay() {
        //Clear the old text so no don't double print
        txtDisplayArea.setText("");

        //Loop through every vehicle currently in ArrayList
        for (Vehicle v : fleet) {
            //Adds each vehicle's information to the text area on a new line
            //this uses the correct toString() for Bike or Scooter!!
            txtDisplayArea.append(v.toString() + "\n");
        }
    }

