    private void registerBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBTNActionPerformed
        // TODO add your handling code here:
        String id = txtID.getText();
        int battery = Integer.parseInt(txtBattery.getText());
        String loc = txtLocation.getText();
        String type = comboType.getSelectedItem().toString(); // "EBike" or "EScooter"

        if (type.equals("EBike")) {
            // Create an EBike and add to the fleet
            EBike newBike = new EBike(id, battery, loc, true);
            fleet.add(newBike);
        } else {
            // Create an EScooter and add to the fleet
            EScooter newScooter = new EScooter(id, battery, loc, 100.0);
            fleet.add(newScooter);
        }

        updateFleetDisplay();

    }//GEN-LAST:event_registerBTNActionPerformed

